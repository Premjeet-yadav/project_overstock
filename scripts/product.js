function displayproduct(furniture_data){
    document.getElementById("parent").innerHTML="";
    furniture_data.map(function(el){
        var div = document.createElement("div");
        var img = document.createElement("img");
        img.setAttribute("src",el.img_url);
        img.setAttribute("alt",el.name);
        img.setAttribute("class","product_image");
        var name = document.createElement("p");
        name.textContent = el.name;
        var price = document.createElement("p");
        price.setAttribute("id","price");
        price.textContent = "Sale INR "+el.price;
        price.style.color = "red";
        div.addEventListener("click",function(){
            navToNextPage(el);
        });
    var rating = document.createElement("div");
    rating.classList.add("rating");

    // Create and append the star icons based on the rating
    for (var i = 1; i <= 5; i++) {
      var star = document.createElement("span");
      star.classList.add("fa", "fa-star");
      if (i <= el.rating) {
        star.classList.add("checked");
      }
      rating.appendChild(star);
    }
        div.append(img,price,rating,name);
     document.getElementById("parent").append(div);
    })
    
}
